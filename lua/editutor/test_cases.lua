function M.get_by_language(lang)
  local result = {}
  for _, tc in ipairs(M.TEST_CASES) do
    if tc.lang == lang then table.insert(result, tc) end
  end
  return result
end

function M.get_by_repo(repo)
  local result = {}
  for _, tc in ipairs(M.TEST_CASES) do
    if tc.repo == repo then table.insert(result, tc) end
  end
  return result
end

function M.get_by_pattern(pattern)
  local result = {}
  for _, tc in ipairs(M.TEST_CASES) do
    if tc.pattern == pattern then table.insert(result, tc) end
  end
  return result
end

function M.get_file_path(tc)
  return M.REPOS_DIR .. "/" .. tc.repo .. "/" .. tc.file
end

function M.get_stats()
  local stats = {total = #M.TEST_CASES, by_language = {}, by_pattern = {}, by_repo = {}}
  for _, tc in ipairs(M.TEST_CASES) do
    stats.by_language[tc.lang] = (stats.by_language[tc.lang] or 0) + 1
    stats.by_pattern[tc.pattern] = (stats.by_pattern[tc.pattern] or 0) + 1
    stats.by_repo[tc.repo] = (stats.by_repo[tc.repo] or 0) + 1
  end
  return stats
end

function M.validate()
  local results = {valid = {}, invalid = {}}
  for _, tc in ipairs(M.TEST_CASES) do
    local path = M.get_file_path(tc)
    if vim.fn.filereadable(path) == 1 then
      table.insert(results.valid, {test_case = tc, path = path})
    else
      table.insert(results.invalid, {test_case = tc, path = path, error = "File not found"})
    end
  end
  return results
end

return M
